if ( ! function_exists('validator')) {
	function validator($arr) {
		$check = true;
		foreach ($arr as $value) {
			if (empty($value)) {
				$check = false;
				break;
			}
		}
		return $check;
	}
}

if ( ! function_exists('captcha')) {
	function captcha() {
		if( ! empty($_POST['captcha'])) {
			return $_POST['captcha'] === $_SESSION['captcha'];
		}
	}
}